#!/usr/bin/env python3
"""
Development Templates for V8 Code Generator

Provides 5 new template types:
1. test_runner - Auto-run tests before git commit
2. linter_precommit - Format/lint code before push  
3. dependency_update - Check for outdated packages weekly
4. build_optimization - Cache build artifacts
5. hot_reload - Auto-restart dev server on file changes
"""

from pathlib import Path
from typing import Dict


def template_test_runner(pattern: Dict, output_dir: Path) -> Dict:
    """Generate automated test runner for pre-commit"""
    test_command = pattern.get('test_command', 'npm test')
    project_dir = pattern.get('project_dir', '.')
    
    script_name = "run_tests_precommit"
    script_path = output_dir / f"{script_name}.sh"
    
    code = f'''#!/bin/bash
#
# Auto-generated test runner (pre-commit hook)
# Generated by V8 Code Generator
# Pattern confidence: {pattern.get('confidence', 0.0):.0%}
#

PROJECT_DIR="{project_dir}"
TEST_CMD="{test_command}"

cd "$PROJECT_DIR" || exit 1

echo "🧪 Running tests before commit..."

if $TEST_CMD; then
    echo "✅ All tests passed"
    exit 0
else
    echo "❌ Tests failed - commit blocked"
    echo "Fix failing tests before committing"
    exit 1
fi
'''
    
    install = f'''# Installation:
1. Save to: {script_path}
2. Make executable: chmod +x {script_path}
3. Install as git hook:
   ln -sf {script_path} {project_dir}/.git/hooks/pre-commit
4. Tests will run automatically before each commit
'''
    
    return {
        'script_path': script_path,
        'script_name': script_name,
        'code': code,
        'language': 'bash',
        'install_instructions': install,
        'estimated_savings': '30 min/week'
    }


